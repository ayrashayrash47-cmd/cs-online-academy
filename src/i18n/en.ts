// Widen literal string types (from `as const`) back to `string` so other
// locales can provide their own translated text with the same shape.
type Widen<T> = T extends string
  ? string
  : T extends readonly (infer U)[]
  ? readonly Widen<U>[]
  : T extends object
  ? { [K in keyof T]: Widen<T[K]> }
  : T;

export type Dictionary = Widen<typeof en>;
